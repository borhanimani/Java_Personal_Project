    public void fillTotalPaymentsTable() {
        ArrayList<User> users = new ArrayList<>();
        String[] tableFields = {"ID", "Name", "Username", "Total Payment"};
        DefaultTableModel tableModel = new DefaultTableModel(tableFields, 0);
        totalPaymentsTable.setModel(tableModel);

        if (totalPaySortByNameRadioButton.isSelected()) {
            users = this.mobileCompany.sortUsers();
        } else {
            users = this.mobileCompany.sortUsersByMonthlyPayment();
        }
        for (int i = 0; i < users.size(); i++) {
            User user = users.get(i);
            String[] tableRowFields = new String[4];
            tableRowFields[0] = user.getUserID() + "";
            tableRowFields[1] = user.getName();
            tableRowFields[2] = user.getUsername();
            tableRowFields[3] = String.format("$%.2f", user.calcTotalpaymentForEachUser(this.mobileCompany.getFlatRate()));
            tableModel.addRow(tableRowFields);
        }
    }
