    private void jTabbedPane2StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane2StateChanged
        //report tab
        companyAdminNameLabel.setText("Report For " + mobileCompany.getCompanyName() + " Co.");

        //find user tab
        userItemComboBox.setModel(new DefaultComboBoxModel<String>());
        for (User user : this.mobileCompany.getUsers().values()) {
            userItemComboBox.addItem(user.getUsername() + ": " + user.getUserID());
        }

        // add user tab
        citiesComboBox.setModel(new DefaultComboBoxModel<String>(citiesList.toArray(new String[0])));
        citiesComboBox.setSelectedIndex(0);

        //add plan tab
        personalPlanRadioButton.setSelected(true);
        if (personalPlanRadioButton.isSelected()) {
            planNumberOfEmployeesLabel.setVisible(false);
            planNOPText.setVisible(false);
        }
        userChoiceForAddPlanComboBox.setModel(new DefaultComboBoxModel<String>());
        for (User user : this.mobileCompany.getUsers().values()) {
            userChoiceForAddPlanComboBox.addItem(user.getUsername() + ": " + user.getUserID());
        }

        //user information tab
        fillUserInformationTable();

        //plan information tab
        planInformationUsersComboBox.setModel(new DefaultComboBoxModel<String>());
        for (User user : this.mobileCompany.getUsers().values()) {
            planInformationUsersComboBox.addItem(user.getUsername() + ": " + user.getUserID());
        }
        fillPlanInformationTable();

        //filter Plans
        FilterPlanUsersComboBox.setModel(new DefaultComboBoxModel<String>());
        for (User user : this.mobileCompany.getUsers().values()) {
            FilterPlanUsersComboBox.addItem(user.getUsername() + ": " + user.getUserID());
        }
        fillFilteredTable();

        // total payments tab
        fillTotalPaymentsTable();
        totalPaySortByNameRadioButton.setSelected(true);

        //admin tab
        adminCompanyTextBox.setText(this.mobileCompany.getCompanyName());
        adminNameTextBox.setText(this.mobileCompany.getAdminUsername());
        adminPasswordTextBox.setText(this.mobileCompany.getAdminPassword());
        adminFlatrateTextBox.setText(this.mobileCompany.getFlatRate() + "");
    }//GEN-LAST:event_jTabbedPane2StateChanged

